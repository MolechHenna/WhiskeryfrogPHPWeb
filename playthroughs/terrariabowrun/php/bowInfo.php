class BowInfo
{
    public $name;
    public $rarity;
    public $image;
    public $damage;
    public $velocity;
    public $usetime;
    public $knockback;
    public $critrate;
    public $sell;
    public $materials;

    function __construct($name, $rarity, $image, $damage, $velocity, $usetime, $knockback, $critrate, $sell, $materials)
    {
        $this->name = $name;
        $this->rarity = $rarity;
        $this->image = $image;
        $this->damage = $damage;
        $this->velocity = $velocity;
        $this->usetime = $usetime;
        $this->knockback = $knockback;
        $this->critrate = $critrate;
        $this->sell = $sell;
        $this->materials = $materials;
    }
}

